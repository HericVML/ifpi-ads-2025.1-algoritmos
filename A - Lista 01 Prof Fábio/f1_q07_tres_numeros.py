# Entrada
numero_1 = float(input('Número 1: '))
numero_2 = float(input('Número 2: '))
numero_3 = float(input('Número 3: '))

# Processamento
soma_1_e_2 = numero_1 + numero_2
diferenca_2_e_3 = numero_2 - numero_3

# Saída
print(f'Soma dos dois primeiros números: {soma_1_e_2}')
print(f'Diferença entre os dois últimos números: {diferenca_2_e_3}')